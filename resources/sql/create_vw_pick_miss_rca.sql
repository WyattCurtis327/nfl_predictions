-- Join graded misses with deterministic RCA for Genie and the weekly picks app.
CREATE OR REPLACE VIEW {catalog}.{predictions_schema}.pick_miss_rca AS
SELECT
  r.rca_id,
  r.rca_run_id,
  r.grading_run_id,
  g.grade_id,
  g.prediction_id,
  g.game_id,
  g.season,
  g.week,
  g.game_type,
  g.gameday,
  g.away_abbr,
  g.home_abbr,
  g.spread_pick,
  g.total_pick,
  g.spread_confidence,
  g.total_confidence,
  g.spread_correct,
  g.total_correct,
  g.actual_away_score,
  g.actual_home_score,
  g.actual_total,
  g.actual_winner,
  g.away_spread,
  g.home_spread,
  g.total_line,
  g.proj_away_score,
  g.proj_home_score,
  g.proj_total,
  r.miss_types,
  r.primary_cause,
  r.pbp_proj_home,
  r.pbp_proj_away,
  r.nfelo_proj_home,
  r.nfelo_proj_away,
  r.market_proj_home,
  r.market_proj_away,
  r.actual_home,
  r.actual_away,
  r.home_profile_games,
  r.home_profile_pf_mean,
  r.away_profile_games,
  r.away_profile_pf_mean,
  r.game_play_count,
  r.game_home_turnovers,
  r.game_away_turnovers,
  r.game_home_epa,
  r.game_away_epa,
  r.profile_source,
  r.projection_source,
  r.cause_summary,
  r.analyzed_at,
  g.graded_at
FROM {catalog}.{predictions_schema}.prediction_grades g
INNER JOIN {catalog}.{predictions_schema}.prediction_rca r
  ON g.grade_id = r.grade_id
WHERE g.spread_correct = FALSE OR g.total_correct = FALSE